        <p style="margin-left: 20px; text-align: justify;">(5) &nbsp; &nbsp;회원정보 및 웹서비스 혹은 애플리케이션 등에 관한 정보가 변경되었음에도 해당 사항을
            수정하지 않음으로써 발생한 각종 손해와 잘못된 수정으로 인하여 발생한 손해는 당해 회원이 부담하여야 하며, 회사는 이에 대하여 아무런 책임을 지지 않습니다.</p>
        <p style="text-align: justify;"><strong>제15조. 통지</strong></p>
        <p style="margin-left: 20px; text-align: justify;">(1) &nbsp; &nbsp;회사가 회원에게 개별 통지하는 경우, 이 약관에서 달리 정하지 않는 한 전자우편,
            전화, SMS, 중 하나의 방법으로 통지하는 것을 원칙으로 합니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(2) &nbsp; &nbsp;제1항에도 불구하고 회사가 불특정 다수의 회원에게 통지하는 경우 공지사항 화면에 7일
            이상 해당 사실을 게시함으로써 개별 통지에 갈음할 수 있습니다.</p>
        <p style="text-align: justify;"><br></p>
        <h4 style="text-align: justify;"><strong><u>제5장 계약해지 및 이용제한</u></strong></h4>
        <p style="text-align: justify;"><br></p>
        <p style="text-align: justify;"><strong>제16조. 이용계약의 해지 등</strong></p>
        <p style="margin-left: 20px; text-align: justify;">(1) &nbsp; &nbsp;회원은 언제든지 이용계약 해지 신청을 할 수 있으며, 회사는 관련 법령 등이 정하는
            바에 따라 이를 신속히 처리하여야 합니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(2) &nbsp; &nbsp;회원의 해지 처리가 완료된 경우, 회사는 관련 법령 및 개인정보처리방침에 따라 회원
            정보를 보유하는 경우를 제외한 회원의 데이터를 삭제합니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(3) &nbsp; &nbsp;회원의 서비스 이용계약 해지가 완료되는 경우 서비스 운영정책에서 정하는 바에 따라
            탈퇴한 회원의 정보로 다시 가입하는 것이 제한될 수 있습니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(4) &nbsp; &nbsp;회원이 6개월 이상 회원의 웹/애플리케이션 등에서 서비스를 이용한 기록이 없을 경우
            회사는 회원에게 별도 고지 후 본 서비스 이용 계약을 해지할 수 있습니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(5) &nbsp; &nbsp;회사는 회원에게 다음 각 호의 어느 하나에 해당하는 사유가 발생하거나 확인된 경우
            이용계약을 해지할 수 있습니다.</p>
        <p style="margin-left: 40px; text-align: justify;">① &nbsp; &nbsp;회원이 서비스의 원활한 진행을 방해하는 행위를 하거나 시도한 경우</p>
        <p style="margin-left: 40px; text-align: justify;">② &nbsp; &nbsp;회원이 고의로 회사의 영업을 방해한 경우</p>
        <p style="margin-left: 40px; text-align: justify;">③ &nbsp; &nbsp;다른 회원의 권리나 명예, 신용 기타 정당한 이익을 침해하거나 대한민국 법령 또는 선량한
            풍속 기타 사회질서에 위배되는 행위를 한 경우</p>
        <p style="margin-left: 40px; text-align: justify;">④ &nbsp; &nbsp;회원이 관련 법령, 이 약관, 제4조의 약관 외 준칙에서 정한 사항을 위반하여 애플리케이션
            등을 운영, 관리한 경우</p>
        <p style="margin-left: 40px; text-align: justify;">⑤ &nbsp; &nbsp;회원이 이 약관에 위배되는 행위를 하거나 이 약관에서 정한 해지사유가 발생한 경우</p>
        <p style="margin-left: 40px; text-align: justify;">⑥ &nbsp; &nbsp;회원에게 제6조에서 정한 이용계약의 승낙거부사유가 있음이 확인된 경우</p>
        <p style="margin-left: 20px; text-align: justify;">(6) &nbsp; &nbsp;기타 회사가 합리적인 판단에 기하여 서비스의 제공을 거부할 필요가 있다고 인정할 경우
            회사가 이용계약을 해지하는 경우 회사는 회원에게 이메일 등으로 해지 사유를 밝혀 해지의사를 통지합니다. 이 경우 회사는 해지를 하기 전에 상당한 기간을 정하여 회원에게 이의 신청의 기회를 부여합니다.
            다만, 이 약관 및 운영정책에서 정하는 사유가 있을 경우에는 별도의 이의 신청 기간을 부여하지 않을 수 있습니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(7) &nbsp; &nbsp;이용계약이 회사에 의해 해지되는 경우 회원의 재이용 신청에 대하여 회사는 그 승낙을
            거절할 수 있습니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(8) &nbsp; &nbsp;이용계약의 종료와 관련하여 발생한 손해는 이용계약이 종료된 해당 회원이 책임을
            부담하여야 하고, 회사는 관련 법령에 규정이 없는 한 책임을 지지 않습니다.</p>
        <p style="text-align: justify;"><strong>제17조. 서비스 이용제한 등</strong></p>
        <p style="margin-left: 20px; text-align: justify;">(1) &nbsp; &nbsp;회사는 회원이 이 약관 및 운영정책에 따른 의무를 위반하거나 서비스의 정상적인 운영을
            방해한 경우, 경고, 데이터 삭제 요구, 일시정지(플랫폼 기능에 대한 개발자와 웹/앱의 액세스 제한 포함), 이용계약의 해지 등으로 서비스 이용을 단계적으로 제한할 수 있습니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(2) &nbsp; &nbsp;회사는 전항에도 불구하고, 회원이 현행 법령을 위반한 경우에는 즉시 서비스 이용계약을
            해지할 수 있습니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(3) &nbsp; &nbsp;본 조에 따라 서비스 이용을 제한하거나 계약을 해지하는 경우 회사는 제11조에 따라
            회원에게 통지합니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(4) &nbsp; &nbsp;회원은 본 조에 따른 이용제한 등에 대해 회사가 정한 절차에 따라 이의신청을 할 수
            있습니다. 이 때 이의가 정당하다고 회사가 인정하는 경우 회사는 즉시 서비스의 이용을 재개합니다.</p>
        <p style="text-align: justify;"><br></p>
        <h4 style="text-align: justify;"><strong><u>제6장 손해배상 등</u></strong></h4>
        <p style="text-align: justify;"><br></p>
        <p style="text-align: justify;"><strong>제18조. 손해배상</strong></p>
