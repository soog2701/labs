            그의 이용으로 발생할 수 있는 회원의 어떠한 피해에 대하여 그 책임을 부담하지 않습니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(2) &nbsp; &nbsp;회원이 이 약관의 규정을 위반함으로 인하여 회사의 영업활동에 손해가 발생하게 되는 경우
            해당 회원은 회사에 그 손해를 배상해야 할 책임이 있습니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(3) &nbsp; &nbsp;서비스를 이용하면서 불법행위를 하거나 이 약관의 규정을 위반하는 행위를 한 회원으로부터
            회사가 해당 회원 이외의 제3자로부터 손해배상 청구 또는 소송을 비롯한 각종 이의제기를 받는다면 해당 회원은 자신의 책임과 비용으로 회사를 면책시켜야 하며, 회사가 면책되지 못한 경우 해당 회원은 그로
            인하여 회사에 발생한 모든 손해를 배상해야 합니다.</p>
        <p style="text-align: justify;"><strong>제19조. 회사의 책임제한</strong></p>
