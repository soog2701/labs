        <p style="margin-left: 20px; text-align: justify;">(2) &nbsp; &nbsp;회사는 회원의 귀책사유로 인한 서비스 이용의 장애에 대하여 책임을 지지 않습니다.
        </p>
        <p style="margin-left: 20px; text-align: justify;">(3) &nbsp; &nbsp;회사는 서비스 이용과 관련하여 회원 상호간 또는 회원과 제3자 상호간 발생한 분쟁에
            대해서는 개입할 의무가 없으며 이로 인한 손해에 대하여 어떠한 책임도 부담하지 않습니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(4) &nbsp; &nbsp;회원이 자신의 개인정보, 계정정보(아이디(ID), 비밀번호), 인증키 등을 타인에게
            제공하거나, 회원의 관리소홀로 유출됨으로써 발생하는 피해에 대해서 회사는 책임을 지지 않습니다.</p>
        <p style="margin-left: 20px; text-align: justify;">(5) &nbsp; &nbsp;회사는 무료로 제공되는 서비스 이용과 관련하여 관련법령에 특별한 규정이 없는 한 책임을
            부담하지 않습니다.</p>
